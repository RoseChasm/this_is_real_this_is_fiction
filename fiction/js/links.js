


 $(document).ready(function () {
       $('#story5').on('mouseenter', function () {
           $('#kings').fadeIn("4000");
           $('#title').hide();
           $(this).css({
               "text-decoration": "underline"
           });
       }).on('mouseleave', function () {
           $('#kings').fadeOut("4000");
           $('#title').fadeIn("6000");
           $(this).css({
               "text-decoration": ''
           });
       });;
   });

